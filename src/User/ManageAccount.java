    private void filterInput(KeyEvent evt) {
    
         int k = evt.getKeyCode();
         char c = evt.getKeyChar();
      if (!((c >= '0') && (c <= '9') ||
         (c == KeyEvent.VK_ENTER)|| c == KeyEvent.VK_BACK_SPACE)) {
          JOptionPane.showMessageDialog(null,"Phone Number Required Please");
        evt.consume();
      }
    }
    
      
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if (jRadioButton1.isSelected()){
              status = 0;
         }
         else if(jRadioButton2.isSelected()){
             status = 1;
         }
         
           if (CheckState()){
            jLabel5.setText("Please fill in all the fields above");
            
        }
        else 
        {
            jLabel5.setText("Done");
           
                 
        // TODO add your handling code here:
        String firstname = jTextField1.getText();
        String lastname = jTextField2.getText();
        int telephone = Integer.parseInt(jTextField3.getText());
        String address = jTextField4.getText();
        String username = jTextField8.getText();
        String pwd = jPasswordField1.getText();
        String pwd1 = jPasswordField2.getText();
        
        if (pwd.equals(pwd1)){
        Calendar cal = Calendar.getInstance();
       Formatter pp = new Formatter();
        
       try {
           
            database.setQuery("Select USER_ID from KHAMALS.LOGIN");
            if (database.getResultSet().first()){
                
                String users = database.getResultSet().getString(1);
                if (jTextField8.getText().equals(users)){
                    JOptionPane.showMessageDialog(this, "This username is already taken.");
                }
            }
            database.setQuery("Insert into Login(First_name,LAST_NAME,TELEPHONE,ADDRESS,USER_ID,PD,STATUS,LAST_LOGIN) values('"+firstname+"','"+lastname+"',"+telephone+",'"+address+"','"+username+"','"+pwd+"',"+status+",'"+pp.format("%tF", cal.getTime())+"')");
            JOptionPane.showMessageDialog(this, "USER Succesfully added with Username "+username+" and Password "+pwd+"");
            pp.close();
          
       
       }catch(SQLException e){
         //  JOptionPane.showMessageDialog(null, e);
          e.printStackTrace();
       }
        }
        else
        {
            JOptionPane.showMessageDialog(ManageAccount.this, "The passwords entered do not match");

        }
        
        
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

